
public class RelationOperators {

	public static void main(String[] args) {
		
		int kor = 70, eng = 40, math = 70;
		boolean rst1, rst2, rst3, rst4;
		
		rst1 = kor == eng;
		rst2 = kor != eng;
		rst3 = kor > eng;
		rst4 = kor >= math;
		
		System.out.println(rst1 + "," + rst2 + "," + rst3 + "," + rst4);
		
	}

}
